package ArrayPacks;

public class ArraysCls2 {
    public static void main(String[] args) {

        int[] ededler = new int[5];

        ededler[0] = 45;
        ededler[1] = 23;
        ededler[2] = 44;
        ededler[3] = 54;
        ededler[4] = 87;

        for (int eded : ededler){
            System.out.println(eded);
        }

    }
}
